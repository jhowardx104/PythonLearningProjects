# basic math = follows order of operations
number = 1 + 2 + 3 / 4.0
print(number)

# modulo works here
remainder = 11 % 3
print(remainder)

# exponents are easy
squared = 7 ** 2
cubed = 7 ** 3
print(squared)
print(cubed)

# concatenation
aString = "this" + " " + "string"
print(aString)

# you can multiply strings?!
aMultipliedString = "hello" * 10
print(aMultipliedString)

# joining lists
even_numbers = [2, 4, 6, 8]
odd_numbers = [1, 3, 5, 7]
all_numbers = odd_numbers + even_numbers
print(all_numbers)

# mulitplying (repeating lists)
print([1, 2, 3] * 3)
